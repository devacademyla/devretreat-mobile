package com.iluminatic.modelo;

public class Configuracion {

	
	private String id;
	private String planMb;
	private String diaInicioFacturacion;
	private String notificacion50;
	private String usoMB;
	
	public String getUsoMB() {
		return usoMB;
	}
	public void setUsoMB(String usoMB) {
		this.usoMB = usoMB;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPlanMb() {
		return planMb;
	}
	public void setPlanMb(String planMb) {
		this.planMb = planMb;
	}
	public String getDiaInicioFacturacion() {
		return diaInicioFacturacion;
	}
	public void setDiaInicioFacturacion(String diaInicioFacturacion) {
		this.diaInicioFacturacion = diaInicioFacturacion;
	}
	public String getNotificacion50() {
		return notificacion50;
	}
	public void setNotificacion50(String notificacion50) {
		this.notificacion50 = notificacion50;
	}
	
}
